"""
Introdução a funções (def)
Funções são trechos  de código usados para
replicar determinada ação ao longo do seu código
Elas podem receber valores para parâmetros (argumentos)
e retornar um valor especifico
Por padrão as funções retornam None (vazio)
"""
def imprimir(a,b,c):
    print(a/b+c)

imprimir(16,4,8) #faz a função imprimir 12.0
imprimir(10,5,2) #faz a função imprimir 4.0
imprimir(25,5,5) #faz a função imprimir 10.0
